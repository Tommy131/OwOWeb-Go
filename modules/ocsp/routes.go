package ocsp

import (
	"github.com/gin-gonic/gin"
)

func SetupRoutes(router *gin.Engine) {
	globalGroup := router.Group("/ocsp")
	{
		globalGroup.POST("/", OCSPHandler)
		globalGroup.GET("/:ocspRequest", OCSPRequestHandler)
	}
}
